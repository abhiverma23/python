x = range(10)
print(x)
print(type(x))
for p in x:
    print(p)

y = range(10,20)
print(y)
print(type(y))
for p in y:
    print(p)

z = range(20,30,2)
print(z)
print(type(z))
for p in z:
    print(p)

a = range(30,40,-1)
print(a)
print(type(a))
for p in a:
    print(p)

b = range(40,30,-1)
print(b)
print(type(b))
for p in b:
    print(p)
